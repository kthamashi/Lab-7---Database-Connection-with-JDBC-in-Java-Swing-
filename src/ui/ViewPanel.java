    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedIndex = userTable.getSelectedRow();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user to delete", "Error deleting user", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            selectedUser = userList.get(selectedIndex);
            DatabaseConnector.deleteUser(selectedUser);
            JOptionPane.showMessageDialog(null, "User Deleted Successfully", "Succesfully Deleted", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            populateTable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error while Registering!" + e.getMessage());

        }
    }//GEN-LAST:event_deleteBtnActionPerformed

