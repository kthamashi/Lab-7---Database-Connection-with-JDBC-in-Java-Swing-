    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        User user = new User();
        try {
            user.setName(nameTxtfield.getText());
            user.setAge(Integer.parseInt(ageTxtfield.getText()));
            DatabaseConnector.editUser(selectedUser, user);
            JOptionPane.showMessageDialog(null, "User Updated Successfully", "Succesful Update", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            populateTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }

    }//GEN-LAST:event_saveBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int selectedIndex = userTable.getSelectedRow();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Please select a user to update", "Error updating user", JOptionPane.ERROR_MESSAGE);
            return;
        }
        selectedUser = userList.get(selectedIndex);
        nameTxtfield.setText(selectedUser.getName());
        ageTxtfield.setText(Integer.toString(selectedUser.getAge()));
    }//GEN-LAST:event_updateBtnActionPerformed

